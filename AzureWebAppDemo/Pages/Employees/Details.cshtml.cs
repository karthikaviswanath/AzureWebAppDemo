using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using AzureWebAppDemo.Data;
using AzureWebAppDemo.Entities;

namespace AzureWebAppDemo.Pages.Employees
{
    public class DetailsModel : PageModel
    {
        private readonly AzureWebAppDemo.Data.AzureAppDbContext _context;

        public DetailsModel(AzureWebAppDemo.Data.AzureAppDbContext context)
        {
            _context = context;
        }

      public Employee Employee { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Employees == null)
            {
                return NotFound();
            }

            var employee = await _context.Employees.FirstOrDefaultAsync(m => m.Id == id);
            if (employee == null)
            {
                return NotFound();
            }
            else 
            {
                Employee = employee;
            }
            return Page();
        }
    }
}
